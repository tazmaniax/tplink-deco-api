def get_endpoint(name: str) -> EndpointSpec:
    """Return one documented operation by its stable dotted name."""
    try:
        return _BY_NAME[name]
    except KeyError as exc:
        raise KeyError(f"Unknown Deco endpoint operation: {name}") from exc
