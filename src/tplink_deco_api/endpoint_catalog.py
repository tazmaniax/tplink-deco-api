def _specs(
    path: str,
    form: str,
    operations: tuple[str, ...],
    *,
    authentication: AuthenticationMode = "encrypted",
    response_kind: ResponseKind = "object",
    safety: SafetyLevel = "read_only",
    sensitivity: SensitivityLevel = "normal",
    default_params: JsonObject | None = None,
    media_type: str = "application/json",
    form_selector: bool = True,
) -> tuple[EndpointSpec, ...]:
    endpoint_specs: list[EndpointSpec] = []
    for operation in operations:
        contract_key = (path, form, operation)
        required_params, optional_params = _PARAMETER_CONTRACTS.get(
            contract_key,
            ((), ()),
        )
        endpoint_specs.append(
            EndpointSpec(
                path=path,
                form=form,
                operation=operation,
                authentication=authentication,
                response_kind=response_kind,
                safety=safety,
                sensitivity=sensitivity,
                default_params=default_params,
                documentation=_DOCUMENTATION.get(path, "docs/endpoints/README.md"),
                media_type=media_type,
                required_params=required_params,
                optional_params=optional_params,
                contract_source=(
                    "firmware_asset"
                    if contract_key in _FIRMWARE_ASSET_CONTRACTS
                    else "documented"
                    if required_params or optional_params
                    else "none"
                ),
                form_selector=form_selector,
            )
        )
    return tuple(endpoint_specs)


